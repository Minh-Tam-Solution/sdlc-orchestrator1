interface SidebarProps {
  collapsed?: boolean;
  onToggle?: () => void;
}

export function Sidebar({ collapsed = false, onToggle }: SidebarProps) {
  const pathname = usePathname();
  const [isCollapsed, setIsCollapsed] = useState(collapsed);
  const { user } = useAuth();

  // Only show Admin Panel link for superusers
  const isSuperuser = user?.is_superuser;

  const handleToggle = () => {
    setIsCollapsed(!isCollapsed);
    onToggle?.();
  };

  return (
    <div
      className={cn(
        "flex h-full flex-col border-r border-gray-200 bg-white transition-all duration-300",
        isCollapsed ? "w-16" : "w-64"
      )}
    >
      {/* Logo */}
      <div className="flex h-16 items-center justify-between border-b border-gray-200 px-4">
        {!isCollapsed && (
          <Link href="/app" className="flex items-center gap-2">
            <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-blue-600">
              <span className="text-sm font-bold text-white">S</span>
            </div>
            <span className="text-lg font-semibold text-gray-900">SDLC</span>
          </Link>
        )}
        <button
          onClick={handleToggle}
          className="rounded-lg p-1.5 text-gray-500 hover:bg-gray-100 hover:text-gray-700"
        >
          {isCollapsed ? (
            <ChevronRightIcon className="h-5 w-5" />
          ) : (
            <ChevronLeftIcon className="h-5 w-5" />
          )}
        </button>
      </div>

      {/* Navigation */}
      <nav className="flex-1 space-y-1 px-2 py-4">
        {navigationItems.filter((item) => {
          // Sprint 226: OTT Gateway is admin-only — hide for non-admin users
          if (item.href === "/app/ott-gateway" && !isSuperuser) return false;
          return true;
        }).map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(item.href + "/");
          return (
            <Link
              key={item.name}
              href={item.href}
              className={cn(
                "flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors",
                isActive
                  ? "bg-blue-50 text-blue-700"
                  : "text-gray-700 hover:bg-gray-100 hover:text-gray-900"
              )}
              title={isCollapsed ? item.name : undefined}
            >
              <item.icon
                className={cn(
                  "h-5 w-5 flex-shrink-0",
                  isActive ? "text-blue-700" : "text-gray-400"
                )}
              />
              {!isCollapsed && <span>{item.name}</span>}
            </Link>
          );
        })}

        {/* Divider */}
        <div className="my-4 border-t border-gray-200" />

        {/* Settings - always visible */}
        <Link
          href="/app/settings"
          className={cn(
            "flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors",
            pathname === "/app/settings" || pathname.startsWith("/app/settings/")
              ? "bg-blue-50 text-blue-700"
              : "text-gray-700 hover:bg-gray-100 hover:text-gray-900"
          )}
          title={isCollapsed ? "Settings" : undefined}
        >
          <CogIcon
            className={cn(
              "h-5 w-5 flex-shrink-0",
              pathname === "/app/settings" || pathname.startsWith("/app/settings/")
                ? "text-blue-700"
                : "text-gray-400"
            )}
          />
          {!isCollapsed && <span>Settings</span>}
        </Link>

        {/* Admin Section - only for superusers (Sprint 86: System Settings) */}
        {isSuperuser && (
          <>
            {/* Admin Panel - User Management */}
            <Link
              href="/admin"
              className={cn(
                "flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors",
                pathname === "/admin" && !pathname.includes("/app/admin")
                  ? "bg-red-50 text-red-700"
                  : "text-gray-700 hover:bg-gray-100 hover:text-gray-900"
              )}
              title={isCollapsed ? "Admin Panel" : undefined}
            >
              <UsersIcon
                className={cn(
                  "h-5 w-5 flex-shrink-0",
                  pathname === "/admin" && !pathname.includes("/app/admin")
                    ? "text-red-700"
                    : "text-gray-400"
                )}
              />
              {!isCollapsed && <span>Admin Panel</span>}
            </Link>
            {/* System Settings - Sprint 86 Phase 2 */}
            <Link
              href="/app/admin/settings"
              className={cn(
                "flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors",
                pathname === "/app/admin/settings" || pathname.startsWith("/app/admin/settings/")
                  ? "bg-amber-50 text-amber-700"
                  : "text-gray-700 hover:bg-gray-100 hover:text-gray-900"
              )}
              title={isCollapsed ? "System Settings" : undefined}
            >
              <AdjustmentsIcon
                className={cn(
                  "h-5 w-5 flex-shrink-0",
                  pathname === "/app/admin/settings" || pathname.startsWith("/app/admin/settings/")
                    ? "text-amber-700"
                    : "text-gray-400"
                )}
              />
              {!isCollapsed && <span>System Settings</span>}
            </Link>
          </>
        )}
      </nav>

      {/* Footer */}
      {!isCollapsed && (
        <div className="border-t border-gray-200 p-4">
          <p className="text-xs text-gray-500">SDLC Orchestrator v1.2</p>
        </div>
      )}
    </div>
  );
}
