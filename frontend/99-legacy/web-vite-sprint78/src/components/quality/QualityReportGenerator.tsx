function generateReportId(): string {
  const timestamp = Date.now().toString(36);
  const random = Math.random().toString(36).substring(2, 8);
  return `QR-${timestamp}-${random}`.toUpperCase();
}

function calculateQualityScore(result: PipelineResult): number {
  let score = 100;
  let penalty = 0;

  for (const gate of result.gates) {
    if (gate.status === "failed") {
      penalty += 25;
    } else if (gate.status === "skipped") {
      penalty += 5;
    }

    if (isSecurityResult(gate.details)) {
      penalty += gate.details.criticalCount * 10;
      penalty += gate.details.highCount * 5;
      penalty += gate.details.mediumCount * 2;
      penalty += gate.details.lowCount * 0.5;
    }

    if (isSyntaxResult(gate.details)) {
      const errorRate =
        (gate.details.filesChecked - gate.details.filesPassed) /
        Math.max(gate.details.filesChecked, 1);
      penalty += errorRate * 20;
    }

    if (isTestResult(gate.details)) {
      const failRate =
        gate.details.testsFailed / Math.max(gate.details.testsRun, 1);
      penalty += failRate * 15;
    }

    if (isArchitectureResult(gate.details)) {
      penalty += gate.details.issues.length * 3;
    }
  }

  return Math.max(0, Math.min(100, Math.round(score - penalty)));
}

function getQualityGrade(score: number): string {
  if (score >= 90) return "A";
  if (score >= 80) return "B";
  if (score >= 70) return "C";
  if (score >= 60) return "D";
  return "F";
}

function getIssueSummary(result: PipelineResult): QualityReport["issueSummary"] {
  const summary = { critical: 0, high: 0, medium: 0, low: 0, total: 0 };

  for (const gate of result.gates) {
    if (isSecurityResult(gate.details)) {
      summary.critical += gate.details.criticalCount;
      summary.high += gate.details.highCount;
      summary.medium += gate.details.mediumCount;
      summary.low += gate.details.lowCount;
    }
    if (isSyntaxResult(gate.details)) {
      summary.high += gate.details.issues.length;
    }
    if (isArchitectureResult(gate.details)) {
      summary.medium += gate.details.issues.length;
    }
    if (isTestResult(gate.details)) {
      summary.high += gate.details.testsFailed;
    }
  }

  summary.total = summary.critical + summary.high + summary.medium + summary.low;
  return summary;
}

function getGateSummary(result: PipelineResult): QualityReport["gateSummary"] {
  const summary = { passed: 0, failed: 0, skipped: 0, total: result.gates.length };

  for (const gate of result.gates) {
    if (gate.status === "passed") summary.passed++;
    else if (gate.status === "failed") summary.failed++;
    else if (gate.status === "skipped") summary.skipped++;
  }

  return summary;
}

function getFilesAnalyzed(result: PipelineResult): number {
  let files = 0;

  for (const gate of result.gates) {
    if (isSyntaxResult(gate.details)) {
      files = Math.max(files, gate.details.filesChecked);
    }
  }

  return files;
}

function generateRecommendations(
  result: PipelineResult,
  issueSummary: QualityReport["issueSummary"]
): ReportRecommendation[] {
  const recommendations: ReportRecommendation[] = [];
  let priority = 1;

  // Critical security issues
  if (issueSummary.critical > 0) {
    recommendations.push({
      priority: priority++,
      category: "security",
      title: "Fix Critical Security Vulnerabilities",
      vietnameseTitle: "Sửa lỗ hổng bảo mật nghiêm trọng",
      description: `${issueSummary.critical} critical security issue(s) must be addressed immediately before deployment.`,
      vietnameseDescription: `${issueSummary.critical} lỗ hổng bảo mật nghiêm trọng cần được khắc phục ngay trước khi triển khai.`,
    });
  }

  // High severity issues
  if (issueSummary.high > 0) {
    recommendations.push({
      priority: priority++,
      category: "general",
      title: "Address High Severity Issues",
      vietnameseTitle: "Khắc phục vấn đề mức độ cao",
      description: `${issueSummary.high} high severity issue(s) should be fixed before release.`,
      vietnameseDescription: `${issueSummary.high} vấn đề mức độ cao nên được sửa trước khi phát hành.`,
    });
  }

  // Failed gates
  for (const gate of result.gates) {
    if (gate.status === "failed") {
      const config = GATE_CONFIGS[gate.gateName];
      recommendations.push({
        priority: priority++,
        category: gate.gateName,
        title: `Fix ${config.label} Gate Failures`,
        vietnameseTitle: `Sửa lỗi cổng ${config.vietnameseLabel}`,
        description: `The ${config.label.toLowerCase()} validation gate failed. Review and fix the reported issues.`,
        vietnameseDescription: `Cổng xác thực ${config.vietnameseLabel.toLowerCase()} đã thất bại. Xem xét và sửa các vấn đề được báo cáo.`,
      });
    }
  }

  // Test coverage
  for (const gate of result.gates) {
    if (gate.gateName === "tests" && isTestResult(gate.details)) {
      const passRate = gate.details.testsRun > 0
        ? (gate.details.testsPassed / gate.details.testsRun) * 100
        : 0;

      if (passRate < 80) {
        recommendations.push({
          priority: priority++,
          category: "tests",
          title: "Improve Test Coverage",
          vietnameseTitle: "Cải thiện độ phủ kiểm thử",
          description: `Test pass rate is ${passRate.toFixed(1)}%. Aim for at least 80% test coverage.`,
          vietnameseDescription: `Tỷ lệ kiểm thử đạt là ${passRate.toFixed(1)}%. Hướng tới ít nhất 80% độ phủ kiểm thử.`,
        });
      }
    }
  }

  // Add general recommendation if all passed
  if (recommendations.length === 0) {
    recommendations.push({
      priority: 1,
      category: "general",
      title: "Code Quality Excellent",
      vietnameseTitle: "Chất lượng code xuất sắc",
      description: "All quality gates passed. Code is ready for review and deployment.",
      vietnameseDescription: "Tất cả các cổng chất lượng đã đạt. Code sẵn sàng để review và triển khai.",
    });
  }

  return recommendations;
}

function generateReport(
  result: PipelineResult,
  metadata: Partial<ReportMetadata> = {}
): QualityReport {
  const qualityScore = calculateQualityScore(result);
  const issueSummary = getIssueSummary(result);

  return {
    metadata: {
      id: metadata.id || generateReportId(),
      title: metadata.title || "Quality Pipeline Report",
      projectName: metadata.projectName,
      branchName: metadata.branchName,
      commitHash: metadata.commitHash,
      generatedBy: metadata.generatedBy,
      generatedAt: metadata.generatedAt || new Date(),
      version: metadata.version || "1.0.0",
    },
    pipelineResult: result,
    qualityScore,
    qualityGrade: getQualityGrade(qualityScore),
    issueSummary,
    gateSummary: getGateSummary(result),
    filesAnalyzed: getFilesAnalyzed(result),
    totalDurationMs: result.totalDurationMs,
    recommendations: generateRecommendations(result, issueSummary),
  };
}

