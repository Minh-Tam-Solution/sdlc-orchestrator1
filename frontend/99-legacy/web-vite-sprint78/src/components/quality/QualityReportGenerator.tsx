export function exportReportAsJSON(report: QualityReport): string {
  return JSON.stringify(report, null, 2);
}

export function exportReportAsCSV(report: QualityReport): string {
  const lines: string[] = [];

  // Header
  lines.push("Quality Report Export");
  lines.push(`Report ID,${report.metadata.id}`);
  lines.push(`Generated At,${report.metadata.generatedAt.toISOString()}`);
  lines.push(`Quality Score,${report.qualityScore}`);
  lines.push(`Quality Grade,${report.qualityGrade}`);
  lines.push("");

  // Issue Summary
  lines.push("Issue Summary");
  lines.push("Severity,Count");
  lines.push(`Critical,${report.issueSummary.critical}`);
  lines.push(`High,${report.issueSummary.high}`);
  lines.push(`Medium,${report.issueSummary.medium}`);
  lines.push(`Low,${report.issueSummary.low}`);
  lines.push(`Total,${report.issueSummary.total}`);
  lines.push("");

  // Gate Summary
  lines.push("Gate Summary");
  lines.push("Gate,Status,Duration (ms)");
  for (const gate of report.pipelineResult.gates) {
    lines.push(`${gate.gateName},${gate.status},${gate.durationMs}`);
  }
  lines.push("");

  // Recommendations
  lines.push("Recommendations");
  lines.push("Priority,Category,Title,Description");
  for (const rec of report.recommendations) {
    lines.push(`${rec.priority},${rec.category},"${rec.title}","${rec.description}"`);
  }

  return lines.join("\n");
}

export function exportReportAsMarkdown(report: QualityReport, vietnamese = false): string {
  const lines: string[] = [];

  // Title
  lines.push(`# ${vietnamese ? "Báo cáo chất lượng" : "Quality Report"}`);
  lines.push("");

  // Metadata
  lines.push(`## ${vietnamese ? "Thông tin báo cáo" : "Report Information"}`);
  lines.push(`- **${vietnamese ? "Mã báo cáo" : "Report ID"}:** ${report.metadata.id}`);
  lines.push(`- **${vietnamese ? "Ngày tạo" : "Generated At"}:** ${report.metadata.generatedAt.toLocaleString()}`);
  if (report.metadata.projectName) {
    lines.push(`- **${vietnamese ? "Dự án" : "Project"}:** ${report.metadata.projectName}`);
  }
  if (report.metadata.branchName) {
    lines.push(`- **${vietnamese ? "Nhánh" : "Branch"}:** ${report.metadata.branchName}`);
  }
  if (report.metadata.commitHash) {
    lines.push(`- **${vietnamese ? "Commit" : "Commit"}:** \`${report.metadata.commitHash}\``);
  }
  lines.push("");

  // Score
  lines.push(`## ${vietnamese ? "Điểm chất lượng" : "Quality Score"}`);
  lines.push(`**${report.qualityScore}/100** (${vietnamese ? "Hạng" : "Grade"}: **${report.qualityGrade}**)`);
  lines.push("");

  // Issue Summary
  lines.push(`## ${vietnamese ? "Tóm tắt vấn đề" : "Issue Summary"}`);
  lines.push(`| ${vietnamese ? "Mức độ" : "Severity"} | ${vietnamese ? "Số lượng" : "Count"} |`);
  lines.push("|----------|-------|");
  lines.push(`| ${vietnamese ? "Nghiêm trọng" : "Critical"} | ${report.issueSummary.critical} |`);
  lines.push(`| ${vietnamese ? "Cao" : "High"} | ${report.issueSummary.high} |`);
  lines.push(`| ${vietnamese ? "Trung bình" : "Medium"} | ${report.issueSummary.medium} |`);
  lines.push(`| ${vietnamese ? "Thấp" : "Low"} | ${report.issueSummary.low} |`);
  lines.push(`| **${vietnamese ? "Tổng" : "Total"}** | **${report.issueSummary.total}** |`);
  lines.push("");

  // Gate Results
  lines.push(`## ${vietnamese ? "Kết quả cổng" : "Gate Results"}`);
  lines.push(`| ${vietnamese ? "Cổng" : "Gate"} | ${vietnamese ? "Trạng thái" : "Status"} | ${vietnamese ? "Thời gian" : "Duration"} |`);
  lines.push("|------|--------|----------|");
  for (const gate of report.pipelineResult.gates) {
    const config = GATE_CONFIGS[gate.gateName];
    const status = gate.status === "passed" ? "Passed" : gate.status === "failed" ? "Failed" : gate.status;
    const statusEmoji = gate.status === "passed" ? "" : gate.status === "failed" ? "" : "";
    lines.push(`| ${vietnamese ? config.vietnameseLabel : config.label} | ${statusEmoji} ${status} | ${gate.durationMs}ms |`);
  }
  lines.push("");

  // Recommendations
  lines.push(`## ${vietnamese ? "Khuyến nghị" : "Recommendations"}`);
  for (const rec of report.recommendations) {
    lines.push(`### ${rec.priority}. ${vietnamese ? rec.vietnameseTitle : rec.title}`);
    lines.push(vietnamese ? rec.vietnameseDescription : rec.description);
    lines.push("");
  }

  return lines.join("\n");
}

